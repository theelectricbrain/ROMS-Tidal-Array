          DO i=IstrR,IendR
          t(i,j,k,1,itemp)=T0(ng)
          t(i,j,k,1,isalt)=S0(ng)
          END DO
        END DO
      END DO
      RETURN
      END SUBROUTINE ana_NLMinitial_tile
